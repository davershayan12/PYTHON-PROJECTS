#Logical operators are tools used in programming and math to combine or compare conditions. They help decide if something is true or false.
'''and operator''' 
#All conditions must be true.
print('10>5 and 4<3: ',10>5 and 4<3)
#      True  and Fasle =>False
print('10>5 and 4>3: ',10>5 and 4>3)  
#      True  and True =>True

'''or operator'''
#At least one condition must be true.
print('10>5  or  4<3: ',10>5 or 4<3)
#      True or Flase=>True
print('10<5  or  4<3: ',10>5 or 4<3)
#      False or Fasle =>True
'''or operator'''
#Flips the truth or false
print('10<5: ',not(10<5))

